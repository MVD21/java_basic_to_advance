class Operation
{
	public static void main(String args[])
	{
		int num1=20;
		int num2=30;
		int sum,multiply,division,subtract=0;
		
		sum=num1+num2;
		System.out.println("Add two numbers :- "+sum);
		
		subtract=num2-num1;
		System.out.println("Subtract two numbers :- "+subtract);

		multiply=num1*num2;
		System.out.println("Multiply two numbers :- "+multiply);

		division=num1/num2;
		System.out.println("Divide two numbers :- "+division);
	}

}